package corejava.stringmethod;

public class String1 {
    public static void main(String[] args) {
        String name1="janani";
        System.out.println(name1);
        char a[]={'j','a','n','a','n','i'};
        System.out.println(a);
        String name2=new String(a);
        System.out.println(name2);             //object for string
        System.out.println("shobi");
        System.out.println("sathish");
        String name3=new String("raji");
        System.out.println(name3);
        String name4="gokul";
        System.out.println(name4.charAt(4));
    }


}
