package corejava.operator;

public class Arithmetic {
    public static void main(String[] args) {
        int a = 4;
        int b = 3;
        int c = a + b;
        int d = a - b;
        int e = a / b;
        int f = a % b;
        int g = a * b;
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
    }
}
